        <div class="grid lg:grid-cols-3 gap-8">
            <!-- Original French -->
            <div
                class="bg-slate-800 p-6 rounded-xl border border-slate-700 opacity-75 hover:opacity-100 transition-opacity"
            >
                <h3
                    class="text-lg font-bold text-slate-400 mb-4 uppercase tracking-wide"
                >
                    The Original French
                </h3>
                <p
                    class="font-serif text-slate-300 italic mb-4 text-sm leading-relaxed"
                >
                    "Il y avait en Vestphalie, dans le château de M. le baron de
                    Thunder-ten-tronckh..."
                </p>
                <ul class="space-y-3 text-sm text-slate-400">
                    <li>
                        • <strong class="text-slate-200"
                            >"Thunder-ten-tronckh":</strong
                        > A deliberately ridiculous fabricated Germanic-sounding
                        name.
                    </li>
                    <li>
                        • <strong class="text-slate-200">"71 Quartiers":</strong
                        > Absurd specificity. 71 quarterings is incredibly noble,
                        yet still not enough (needs 72).
                    </li>
                    <li>
                        • <strong class="text-slate-200"
                            >"L'injure du temps":</strong
                        > Grandiose way of saying "we lost the paperwork."
                    </li>
                </ul>
            </div>

            <!-- Standard Translation -->
            <div
                class="bg-slate-800 p-6 rounded-xl border border-slate-700 opacity-75 hover:opacity-100 transition-opacity"
            >
                <h3
                    class="text-lg font-bold text-red-400 mb-4 uppercase tracking-wide"
                >
                    Standard Translation
                </h3>
                <p
                    class="font-serif text-slate-300 italic mb-4 text-sm leading-relaxed"
                >
                    "In a castle of Westphalia, belonging to the Baron of
                    Thunder-ten-tronckh..."
                </p>
                <ul class="space-y-3 text-sm text-slate-400">
                    <li>
                        • <strong class="text-red-300">Lost Impact:</strong> "Thunder-ten-tronckh"
                        remains but loses the linguistic clash without the French
                        context.
                    </li>
                    <li>
                        • <strong class="text-red-300">Bluntness:</strong> "Good-natured
                        worthy gentleman" misses the ironic formality of "bon et
                        honnête gentilhomme."
                    </li>
                    <li>
                        • <strong class="text-red-300">Literalism:</strong> "Injuries
                        of time" loses the mock-heroic quality.
                    </li>
                </ul>
            </div>

            <!-- Zogbonk Translation -->
            <div
                class="bg-yellow-900/20 p-6 rounded-xl border-2 border-yellow-500 shadow-xl transform scale-105 z-10"
            >
                <h3
                    class="text-lg font-bold text-yellow-400 mb-4 uppercase tracking-wide"
                >
                    The Zogbonk Translation
                </h3>
                <p
                    class="font-serif text-white italic mb-4 text-sm leading-relaxed"
                >
                    "Once upon a time In Westphalia... a young soul with a
                    disposition as gentle as a poem one writes and never
                    shares... The Castle whispered that Candide was the secret
                    lovechild of the Baron’s sister and an aspiring mapmaker who
                    had lost his way. This compass Casanova..."
                </p>
                <ul class="space-y-3 text-sm text-yellow-100">
                    <li>
                        • <strong class="text-white">"Compass Casanova":</strong
                        > A brilliant invention that maintains satirical tone while
                        updating it.
                    </li>
                    <li>
                        • <strong class="text-white"
                            >"Poem one writes and never shares":</strong
                        > Original metaphor capturing gentleness and naivety.
                    </li>
                    <li>
                        • <strong class="text-white">"Barely a shrub":</strong> Recreates
                        genealogical satire funnier than the literal "quarterings."
                    </li>
                </ul>
            </div>
        </div>


<section id="craft" class="py-20 bg-white">
    <div class="max-w-5xl mx-auto px-4 sm:px-6 lg:px-8">
        <div class="text-center mb-16">
            <h2 class="text-3xl lg:text-4xl font-bold text-slate-900 mb-6">
                The Mandate of Craft: <br /> A Blueprint for Writing That Works
            </h2>
            <p class="text-xl text-slate-600 max-w-3xl mx-auto">
                For too long, we've allowed "literary" to become a code word for
                "needlessly complicated." At itscanon.com, we believe in a
                return to first principles.
            </p>
        </div>

        <div class="space-y-16">
            <!-- 1. Clarity -->
            <div class="grid md:grid-cols-[1fr_2fr] gap-8 items-start">
                <div class="bg-indigo-50 p-6 rounded-xl">
                    <h3 class="text-2xl font-bold text-indigo-900 mb-2">
                        1. Clarity
                    </h3>
                    <div class="text-indigo-600 font-medium">
                        The Foundation of Trust
                    </div>
                </div>
                <div class="space-y-4">
                    <div>
                        <span class="font-bold text-slate-900">What it IS:</span
                        >
                        <span class="text-slate-600"
                            >Clarity is the effortless transmission of an idea
                            from the author's mind to the reader's. It is the
                            result of precise word choice, logical sentence
                            structure, and a relentless focus on the core
                            message. A clear writer has done the hard work so
                            the reader doesn't have to. The prose is a clean
                            window pane, not a stained-glass mosaic.</span
                        >
                    </div>
                    <div>
                        <span class="font-bold text-slate-900"
                            >What it is NOT:</span
                        >
                        <span class="text-slate-600"
                            >Clarity is not simplicity. It is not "dumbing down"
                            complex ideas. It is the mastery of them. Anyone can
                            make a simple idea sound simple. A master can make a
                            profoundly complex idea feel clear and inevitable.</span
                        >
                    </div>
                    <div
                        class="bg-slate-50 p-4 rounded-lg border-l-4 border-indigo-500"
                    >
                        <span class="font-bold text-indigo-900"
                            >The Pedagogical Payoff:</span
                        >
                        <span class="text-slate-700"
                            >When students read clear prose, they can
                            immediately engage with the ideas instead of getting
                            bogged down in decoding the language. When they
                            learn to write with clarity, they learn the most
                            important skill in all communication: respecting
                            their audience and earning their trust. We want them
                            to build arguments, not puzzles.</span
                        >
                    </div>
                </div>
            </div>

            <!-- 2. Pacing -->
            <div class="grid md:grid-cols-[1fr_2fr] gap-8 items-start">
                <div class="bg-purple-50 p-6 rounded-xl">
                    <h3 class="text-2xl font-bold text-purple-900 mb-2">
                        2. Pacing
                    </h3>
                    <div class="text-purple-600 font-medium">
                        The Engine of Engagement
                    </div>
                </div>
                <div class="space-y-4">
                    <div>
                        <span class="font-bold text-slate-900">What it IS:</span
                        >
                        <span class="text-slate-600"
                            >Pacing is the author's deliberate control of the
                            story's momentum. It is the rhythm of the narrative,
                            created by the interplay of sentence length, scene
                            duration, and the strategic release of information.
                            Masterful pacing pulls the reader forward, making
                            them a willing participant in the story, whether
                            it's a breakneck chase scene or a moment of quiet,
                            tense reflection.</span
                        >
                    </div>
                    <div>
                        <span class="font-bold text-slate-900"
                            >What it is NOT:</span
                        >
                        <span class="text-slate-600"
                            >Pacing is not just "fast." A story that is all
                            action is as monotonous as one where nothing
                            happens. Pacing is about variation and control. It's
                            knowing when to accelerate and when to hit the
                            brakes to maximize emotional impact.</span
                        >
                    </div>
                    <div
                        class="bg-slate-50 p-4 rounded-lg border-l-4 border-purple-500"
                    >
                        <span class="font-bold text-purple-900"
                            >The Pedagogical Payoff:</span
                        >
                        <span class="text-slate-700"
                            >Teaching pacing is teaching students how to manage
                            their reader's attention. They learn that a short,
                            punchy sentence after a long, descriptive one can
                            land like a thunderclap. They learn to structure
                            their essays and arguments not as a list of points,
                            but as a journey with a beginning, a rising tension,
                            and a powerful conclusion.</span
                        >
                    </div>
                </div>
            </div>

            <!-- 3. Style -->
            <div class="grid md:grid-cols-[1fr_2fr] gap-8 items-start">
                <div class="bg-pink-50 p-6 rounded-xl">
                    <h3 class="text-2xl font-bold text-pink-900 mb-2">
                        3. Style
                    </h3>
                    <div class="text-pink-600 font-medium">
                        The Author's Fingerprint
                    </div>
                </div>
                <div class="space-y-4">
                    <div>
                        <span class="font-bold text-slate-900">What it IS:</span
                        >
                        <span class="text-slate-600"
                            >Style is the author's unique voice, the sum total
                            of their choices in diction, syntax, and tone. It's
                            the personality that lives inside the prose. A
                            strong style is consistent and authentic, creating a
                            distinct and memorable reading experience.</span
                        >
                    </div>
                    <div>
                        <span class="font-bold text-slate-900"
                            >What it is NOT:</span
                        >
                        <span class="text-slate-600"
                            >Style is not ornamentation. It is not a collection
                            of fancy, "writerly" flourishes tacked onto the
                            prose like cheap jewelry. True style is baked into
                            the very structure of the writing; it is inseparable
                            from the meaning and the message.</span
                        >
                    </div>
                    <div
                        class="bg-slate-50 p-4 rounded-lg border-l-4 border-pink-500"
                    >
                        <span class="font-bold text-pink-900"
                            >The Pedagogical Payoff:</span
                        >
                        <span class="text-slate-700"
                            >By exposing students to models of clean, powerful
                            style, we give them permission to develop their own
                            authentic voice. We teach them to move beyond the
                            generic, five-paragraph essay format and to make
                            deliberate choices that reflect their own
                            perspective. We're teaching them not just to convey
                            information, but to express a point of view.</span
                        >
                    </div>
                </div>
            </div>

            <!-- 4. Vocabulary -->
            <div class="grid md:grid-cols-[1fr_2fr] gap-8 items-start">
                <div class="bg-blue-50 p-6 rounded-xl">
                    <h3 class="text-2xl font-bold text-blue-900 mb-2">
                        4. Vocabulary
                    </h3>
                    <div class="text-blue-600 font-medium">
                        The Precision Toolkit
                    </div>
                </div>
                <div class="space-y-4">
                    <div>
                        <span class="font-bold text-slate-900">What it IS:</span
                        >
                        <span class="text-slate-600"
                            >A masterful vocabulary is not about using the
                            biggest word; it is about using the right word. It
                            is the surgical selection of the one perfect term
                            that conveys the exact shade of meaning and emotion
                            intended. It is the difference between a
                            sledgehammer and a scalpel.</span
                        >
                    </div>
                    <div>
                        <span class="font-bold text-slate-900"
                            >What it is NOT:</span
                        >
                        <span class="text-slate-600"
                            >It is not thesaurus abuse. It is not the clumsy
                            insertion of ten-dollar words to sound intelligent.
                            Obscure or misused vocabulary is a sign of
                            insecurity, not mastery.</span
                        >
                    </div>
                    <div
                        class="bg-slate-50 p-4 rounded-lg border-l-4 border-blue-500"
                    >
                        <span class="font-bold text-blue-900"
                            >The Pedagogical Payoff:</span
                        >
                        <span class="text-slate-700"
                            >We teach students that words are tools, each with a
                            specific function. By analyzing an author's precise
                            word choice, they learn the power of specificity. In
                            their own writing, they learn to stop settling for
                            "good" or "sad" and start hunting for the word that
                            truly captures their intent, making their arguments
                            sharper and their descriptions more vivid.</span
                        >
                    </div>
                </div>
            </div>

            <!-- 5. Nuance -->
            <div class="grid md:grid-cols-[1fr_2fr] gap-8 items-start">
                <div class="bg-emerald-50 p-6 rounded-xl">
                    <h3 class="text-2xl font-bold text-emerald-900 mb-2">
                        5. Nuance
                    </h3>
                    <div class="text-emerald-600 font-medium">
                        The Soul of the Story
                    </div>
                </div>
                <div class="space-y-4">
                    <div>
                        <span class="font-bold text-slate-900">What it IS:</span
                        >
                        <span class="text-slate-600"
                            >Nuance is the art of the unsaid. It's the ability
                            to convey complex emotions and ideas through
                            subtext, suggestion, and ambiguity. It is the space
                            between the words where the deepest meaning resides.
                            Nuance trusts the reader to connect the dots and
                            participate in the creation of meaning.</span
                        >
                    </div>
                    <div>
                        <span class="font-bold text-slate-900"
                            >What it is NOT:</span
                        >
                        <span class="text-slate-600"
                            >It is not vagueness. It is not being unclear.
                            Nuance is a deliberate and controlled act of
                            suggestion, built upon a foundation of absolute
                            clarity.</span
                        >
                    </div>
                    <div
                        class="bg-slate-50 p-4 rounded-lg border-l-4 border-emerald-500"
                    >
                        <span class="font-bold text-emerald-900"
                            >The Pedagogical Payoff:</span
                        >
                        <span class="text-slate-700"
                            >This is the highest level of craft. When students
                            learn to recognize and deploy nuance, they move from
                            being writers who simply state facts to writers who
                            can evoke feeling and provoke thought. They learn to
                            build characters who feel real because they are
                            contradictory, and to write essays that don't just
                            present a conclusion, but explore a complex truth.</span
                        >
                    </div>
                </div>
            </div>
        </div>

        <div class="mt-16 text-center text-lg text-slate-600 max-w-3xl mx-auto">
            <p>
                These are not just rules for writing novels. They are the
                foundational principles of effective communication in any field.
                A student who masters these tenets can write a powerful
                analytical essay, a persuasive business proposal, a clear
                scientific report, and a compelling email.
            </p>
        </div>
    </div>
</section>

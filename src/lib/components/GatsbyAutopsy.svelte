            <!-- Felony 1 -->
            <div class="bg-slate-800/50 p-6 rounded-xl border border-slate-700">
                <h3 class="text-xl font-bold text-red-400 mb-2">
                    1. The Misplaced Modifier (The Ambiguous Actor)
                </h3>
                <div class="space-y-2 text-slate-300">
                    <p>
                        <strong class="text-white">The Crime:</strong> Grammatically,
                        the clause "while trying to look pleasantly interested and
                        a little deaf" attaches to "I" (Nick). But context implies
                        it's Tom and Miss Baker.
                    </p>
                    <p>
                        <strong class="text-white">The Verdict:</strong> A fundamental
                        breakdown in sentence mechanics. Is Nick describing himself
                        or them? The grammatical evidence and narrative evidence
                        are in direct conflict.
                    </p>
                </div>
            </div>

            <!-- Felony 2 -->
            <div class="bg-slate-800/50 p-6 rounded-xl border border-slate-700">
                <h3 class="text-xl font-bold text-red-400 mb-2">
                    2. The Logically Fraudulent Metaphor (The "Tangible Body")
                </h3>
                <div class="space-y-2 text-slate-300">
                    <p>
                        <strong class="text-white">The Crime:</strong> "...as if
                        to a vigil beside a perfectly tangible body." A body is,
                        by nature, tangible. The adjective adds nothing but a sense
                        of unearned profundity.
                    </p>
                    <p>
                        <strong class="text-white">The Verdict:</strong> It smells
                        profound without actually being profound. A clunky, pseudo-intellectual
                        flourish.
                    </p>
                </div>
            </div>

            <!-- Felony 3 -->
            <div class="bg-slate-800/50 p-6 rounded-xl border border-slate-700">
                <h3 class="text-xl font-bold text-red-400 mb-2">
                    3. Structural Collapse (The Overloaded Sentence)
                </h3>
                <div class="space-y-2 text-slate-300">
                    <p>
                        <strong class="text-white">The Crime:</strong> The sentence
                        tries to cram four distinct ideas into one clause-choked
                        package. It loses all momentum and focus.
                    </p>
                    <p>
                        <strong class="text-white">The Verdict:</strong> A perfect
                        example of a "mechanically broken" narrative engine.
                    </p>
                </div>
            </div>
        </div>


                <!-- Author's M.O. -->
                <div>
                    <h4
                        class="font-bold text-slate-900 mb-4 text-sm uppercase tracking-wide text-purple-600 border-b border-purple-100 pb-2"
                    >
                        Author's M.O. (Style & Craft)
                    </h4>
                    <ul class="space-y-6 text-slate-600 text-sm">
                        <li>
                            The book starts with "You" in the preface. The first
                            paragraphs of this chapter are present tense. The
                            rest is past tense. What's with the author jumping
                            all over the place? Is this a rookie mistake, or are
                            we supposed to be paying attention to it?
                        </li>
                        <li>
                            You notice that car ride up and down the mountain?
                            The author uses all this dramatic language—"toiled
                            and battled," "sunlight became a blade"—but Noelan
                            isn't in any real danger. The style is all over the
                            place from poetic to sentence fragments. What's up
                            with that?
                        </li>
                        <li>
                            <strong class="text-slate-900">Critical eye:</strong
                            > Which scenes felt authentic? Which felt like familiar
                            genre tropes?
                        </li>
                        <li>What made you smile or chuckle?</li>
                    </ul>
                </div>
                <!-- Evidence & Red Herrings -->
                <div>
                    <h4
                        class="font-bold text-slate-900 mb-4 text-sm uppercase tracking-wide text-purple-600 border-b border-purple-100 pb-2"
                    >
                        Evidence & Red Herrings (The Plot)
                    </h4>
                    <ul class="space-y-6 text-slate-600 text-sm">
                        <li>
                            Awfully convenient running into someone doing a
                            crime because of some extortion racket the second
                            they roll into town. If Pine Ridge has a population
                            of 5,134, is that chance encounter plausible?
                        </li>
                        <li>
                            What was suspicious? What are some possible clues or
                            red herrings?
                        </li>
                        <li>
                            The plum candy makes its appearance. So now we know
                            where the title comes from. But are there any other
                            metaphors in this chapter that actually worked for
                            you?
                        </li>
                        <li>What’s up with the cat? Why bother?</li>
                    </ul>
                </div>
